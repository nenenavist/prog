package data;

import java.io.Serializable;

/**
 * The MusicGenre enum represents different genres of music.
 */
public enum MusicGenre implements Serializable {
    /**
     * Progressive rock music genre.
     */
    BRIT_POP,

    /**
     * Hip hop music genre.
     */
    HIP_HOP,

    /**
     * Psychedelic cloud rap music genre.
     */
    PROGRESSIVE_ROCK,

    /**
     * Punk rock music genre.
     */
    PSYCHEDELIC_CLOUD_RAP,

    /**
     * Brit pop music genre.
     */
    PUNK_ROCK;
}
